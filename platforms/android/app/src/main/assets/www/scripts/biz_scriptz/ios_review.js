function ios_review_hide(){
		$('#nav-welcome').hide();
		$('#nav-page').hide();
		$('#nav-logout').hide();
		$('#nav-login').hide();
		$('#a_link_page').hide();
		$('#a_link_service').hide();
		$('#a_link_shop').hide();
		$('#a_link_gallery').hide();
		$('#biz_btn_home_top_text').hide();
		$('#biz_feature_list').hide();
}
function ios_review_show(){
		$('#nav-welcome').css("visibility","visible");
		$('#nav-page').show();
		$('#nav-logout').show();
		$('#nav-login').show();
		$('#a_link_page').show();
		$('#a_link_service').show();
		$('#a_link_shop').show();
		$('#a_link_gallery').show();
		$('#biz_btn_home_top_text').show();
		$('#biz_feature_list').show();
}

